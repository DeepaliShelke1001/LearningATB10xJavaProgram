package ex_16_Arrays;

public class Lab155_2D_Array {
    public static void main(String[] args) {

        // Multi Dimensional Array - 2d Array is stored in Horizontal(rows) as well as Vertical(columns)

        int[][] array_2d = new int[3][3];

        array_2d[0][0] = 80;
        array_2d[0][1] = 53;
        array_2d[0][2] = 78;

        array_2d[1][0] = 22;
        array_2d[1][1] = 34;
        array_2d[1][2] = 7;

        array_2d[2][0] = 8;
        array_2d[2][1] = 4;
        array_2d[2][2] = 90;
    }
}
