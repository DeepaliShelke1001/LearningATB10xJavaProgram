package ex_10_For_Loop;

public class Lab110_For_Loop {

    public static void main(String[] args) {

        //ICU ==> Initialization, Condition, Updation(Increment/Decrement)
        int a = 4;
        for (; a > 0; a--) {
            System.out.println(a);
        }
    }
}
// output will be
//        4
//        3
//        2
//        1