package ex_10_For_Loop;

public class Lab103_For_Loop {

    public static void main(String[] args) {

        //ICU ==> Initialization, Condition, Updation(Increment/Decrement)

        for (int a = 0; a < 10; a++) {
            System.out.println(a); // Output: 0,1,2,...... 9. Executed 10 times from 0 to 9
        }
    }
}
