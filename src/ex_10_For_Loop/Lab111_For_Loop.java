package ex_10_For_Loop;

public class Lab111_For_Loop {

    public static void main(String[] args) {

        System.out.println("Start");

        for (int a = 0; a < 10; a++) {
            System.out.println("Deepali");
        }
        System.out.println("End");
    }
}

// output will be

// Start
// 10 times Deepali
//End