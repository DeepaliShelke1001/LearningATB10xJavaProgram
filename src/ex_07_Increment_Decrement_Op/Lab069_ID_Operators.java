package ex_07_Increment_Decrement_Op;

public class Lab069_ID_Operators {
    public static void main(String[] args) {

        int a = 10;
        int result = ++a;
        System.out.println(a); //11
        System.out.println(result); //11

        // Method
        // Line No. | Exp(result)  |  a
        // 6 |  NA  | 10
        // 7  | 11 | 11

    }
}
