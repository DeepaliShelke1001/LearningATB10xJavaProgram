package ex_07_Increment_Decrement_Op;

public class Lab070_ID_Operator {
    public static void main(String[] args) {

        int a = 10;
        int result = a++;
        System.out.println(a); //11
        System.out.println(result); //10

        // Line No. | Exp  |  a
        //  6      |   NA   |  10
        //  7       |  result = 10   |  11
        //  8       |  result = 10   |  11

    }
}
